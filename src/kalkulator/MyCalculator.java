    private void tampilkan_hasil (){
    switch(calculator){
        case 1:
        ans = num + Double.parseDouble(txtOut.getText()); 
            txtOut.setText(Double.toString(ans));
            break;
        case 2:
        ans = num  - Double.parseDouble(txtOut.getText());
            txtOut.setText(Double.toString(ans));
            break;         
        case 3:
        ans = num  * Double.parseDouble(txtOut.getText());
            txtOut.setText(Double.toString(ans));
            break;  
        case 4:
        ans = num  / Double.parseDouble(txtOut.getText());
            txtOut.setText(Double.toString(ans));
            break;
    }
}
    
